#include "../includes/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*elem;
	t_list	*tmp;

	if (*lst)
	{
		elem = *lst;
		while (elem)
		{
			(*del)(elem->content);
			tmp = elem->next;
			free(elem);
			elem = tmp;
		}
	}
	*lst = NULL;
}
